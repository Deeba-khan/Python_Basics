#for
#"for" is a keyword, use to create for loop, and use to iterate through sequence like list, tuple, dictionary.

print("Iterating through a list")
mylist = ["john", "joseph", "michel", "marry"]
for i in mylist:  #i is a variable to iterate through list items one by one
    print(i)

print("Iterating through a string")
mystring = "Silk"
for i in mystring:
    print(i)

print("Iterating through a range")
for i in range(10):
    print(i)