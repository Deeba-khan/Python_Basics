#elif
#'elif' is a keyword, it's use in conditional statements(if). If previous condition is not True then try elif condition.
#'elif' is short for 'else if'
a = 50
b = 80
if a > b:
    print("a is greater than b") 
elif a < b:
    print("Performs 'elif' keyword: ", "a is less than b") #The pervious condition is False, so it runs elif