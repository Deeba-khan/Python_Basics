#Exponentiation is a arithmetic operator
# "**" operators use to multiply values
#It is used to make second value power of the first value and then evaluate/solve it

#exponentiation two values

val1 = 3 #val1 and val2 are operands
val2 = 2
exponent = val1 ** val2 #using division "/" operator [ 3**2 = 3x3 = 9]
print("Exponentiation of {} and {} is {}".format(val1, val2, exponent))

