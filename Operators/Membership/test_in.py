#in
#'in' is membership operator, it returns True if specified value is present in the object, else False.

a = "Hello, World"
print("Returns True if 'Hello' in string, else False: ", "Hello" in a)
print("Returns True if 'python' in string, else False: ", "python" in a)

mylist = ["apple", "mango"]
print("Returns True if 'apple' in mylist, else False: ", "apple" in mylist)
print("Returns True if 'o' in mylist, else False: ", "o" in mylist)

print("Returns True if 'M' in Mango, else False: ", "M" in "Mango")
print("Returns True if 'p' in Mango, else False: ", "p" in "Mango")