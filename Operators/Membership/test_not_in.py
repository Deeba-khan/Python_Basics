#not in
#'not in' is membership operator, it returns True if specified value is not present in the object, else False.

a = "Hello, World"
print("Returns True if 'Hello' not in string, else False: ", "Hello" not in a) #False
print("Returns True if 'python' not in string, else False: ", "python" not in a) #True

mylist = ["apple", "mango"]
print("Returns True if 'apple' not in mylist, else False: ", "apple" not in mylist)
print("Returns True if 'o' not in mylist, else False: ", "o" not in mylist)

print("Returns True if 'M' not in Mango, else False: ", "M" not in "Mango")
print("Returns True if 'p' not in Mango, else False: ", "p" not in "Mango")