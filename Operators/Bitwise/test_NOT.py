#NOT - "~"
#Bitwise NOT operator returns the One's complement of the number

#Formula of NOT
# ~a = -(a + 1)

a = 25 #[-(25 + 1) = -(26) = -26]
complementry = ~a 
print("Performs bitwise NOT operator: ", complementry)

b = 100 #[-(100 + 1) = -(101) = -101]
complementry1 = ~b
print("Performs bitwise NOT operator: ", complementry1)